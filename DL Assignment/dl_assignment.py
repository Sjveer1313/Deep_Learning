def failure_analysis(results, dataset_label):

    pred   = results['CustomRNN']['pred']
    actual = results['CustomRNN']['actual']
    errors = np.abs(pred - actual)

    # Threshold: top 10% = "failure region"
    threshold   = np.percentile(errors, 90)
    failure_idx = np.where(errors > threshold)[0]

    fig, axes = plt.subplots(3, 1, figsize=(14, 13))

    # ── Sub-plot 1: Prediction with failure regions marked ─────
    ax1    = axes[0]
    n_show = min(300, len(pred))
    ax1.plot(actual[:n_show], 'k-', linewidth=1.5, label='Actual', alpha=0.9)
    ax1.plot(pred[:n_show],   'r--', linewidth=1.2, label='CustomRNN Prediction', alpha=0.8)
    fail_in_view = failure_idx[failure_idx < n_show]
    ax1.scatter(fail_in_view, actual[fail_in_view],
                color='red', s=25, zorder=5,
                label=f'Top 10% Error Regions ({len(fail_in_view)} pts)')
    ax1.set_title('CustomRNN — Prediction vs Actual  (red dots = worst errors)',
                  fontsize=11)
    ax1.legend(fontsize=9)
    ax1.grid(True, alpha=0.3)
    ax1.set_ylabel('Value (original scale)')

    # ── Sub-plot 2: Absolute error over time ───────────────────
    ax2 = axes[1]
    ax2.fill_between(range(n_show), errors[:n_show],
                     color='darkorange', alpha=0.4)
    ax2.plot(errors[:n_show], color='darkorange', linewidth=1.0)
    ax2.axhline(y=threshold, color='red', linestyle='--', linewidth=1.5,
                label=f'90th pct threshold = {threshold:.3f}')
    ax2.set_title('Absolute Error  |predicted − actual|  over Time', fontsize=11)
    ax2.set_ylabel('Absolute Error')
    ax2.legend(fontsize=9)
    ax2.grid(True, alpha=0.3)

    # ── Sub-plot 3: Error histogram ────────────────────────────
    ax3 = axes[2]
    ax3.hist(errors, bins=40, color='steelblue', edgecolor='white', alpha=0.85)
    ax3.axvline(x=np.mean(errors), color='red',    linestyle='--',
                label=f'Mean error   = {np.mean(errors):.4f}')
    ax3.axvline(x=threshold,       color='orange', linestyle='--',
                label=f'90th pct     = {threshold:.4f}')
    ax3.set_title('Error Distribution', fontsize=11)
    ax3.set_xlabel('Absolute Error')
    ax3.set_ylabel('Frequency')
    ax3.legend(fontsize=9)
    ax3.grid(True, alpha=0.3)

    plt.suptitle(f'Failure Analysis — CustomRNN on {dataset_label}',
                 fontsize=13, fontweight='bold')
    plt.tight_layout()
    save_path = f"plots/{dataset_label.replace(' ', '_')}_failure.png"
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close()

    # Print summary
    bias = np.mean(pred - actual)
    print(f"\n  FAILURE ANALYSIS — {dataset_label}")
    print(f"  {'─'*45}")
    print(f"  Total test samples      : {len(errors)}")
    print(f"  High-error samples (>90th pct) : {len(failure_idx)}  ({100*len(failure_idx)/len(errors):.1f}%)")
    print(f"  Mean absolute error     : {np.mean(errors):.4f}")
    print(f"  Median absolute error   : {np.median(errors):.4f}")
    print(f"  Max absolute error      : {np.max(errors):.4f}")
    print(f"  Error std deviation     : {np.std(errors):.4f}")
    print(f"  Systematic bias         : {bias:+.4f}  ", end="")
    print("(tends to OVER-predict)" if bias > 0 else "(tends to UNDER-predict)")
    print(f"  ✓ Failure plot saved → {save_path}")

    return errors, failure_idx




