def create_windows(data, window_size, prediction_horizon):

    X_list, y_list = [], []
    total_len = len(data)
    for i in range(total_len - window_size - prediction_horizon + 1):
        X_list.append(data[i : i + window_size])
        y_list.append(data[i + window_size : i + window_size + prediction_horizon])
    return np.array(X_list, dtype=np.float32), np.array(y_list, dtype=np.float32)


def preprocess_data(data, window_size, prediction_horizon, train_ratio=0.8):

    # Step 1: Normalize
    scaler     = MinMaxScaler(feature_range=(0, 1))
    data_norm  = scaler.fit_transform(data.reshape(-1, 1)).flatten()

    # Step 2: Create windows
    X, y = create_windows(data_norm, window_size, prediction_horizon)

    # Step 3: Chronological split
    split   = int(len(X) * train_ratio)
    X_train, X_test = X[:split], X[split:]
    y_train, y_test = y[:split], y[split:]

    # Step 4: Convert to tensors
    # RNN/Transformer expects shape (batch, seq_len, input_features)
    X_train_t = torch.FloatTensor(X_train).unsqueeze(-1)   # (N, window, 1)
    X_test_t  = torch.FloatTensor(X_test).unsqueeze(-1)    # (M, window, 1)
    y_train_t = torch.FloatTensor(y_train)                  # (N, horizon)
    y_test_t  = torch.FloatTensor(y_test)                   # (M, horizon)

    print(f"  Train samples : {len(X_train):4d}  |  X shape: {tuple(X_train_t.shape)}")
    print(f"  Test  samples : {len(X_test):4d}  |  y shape: {tuple(y_train_t.shape)}")
    return X_train_t, X_test_t, y_train_t, y_test_t, scaler



class MLP(nn.Module):

    def __init__(self, window_size, hidden_size, prediction_horizon):
        super(MLP, self).__init__()
        self.net = nn.Sequential(
            nn.Linear(window_size, 64),
            nn.ReLU(),
            nn.Dropout(0.2),        # prevent overfitting on small datasets
            nn.Linear(64, 32),
            nn.ReLU(),
            nn.Linear(32, prediction_horizon)
        )

    def forward(self, x):
        # x: (batch, window_size, 1)
        # Flatten sequence → 1D vector. MLP ignores temporal order.
        x = x.squeeze(-1)           # (batch, window_size)
        return self.net(x)          # (batch, prediction_horizon)


class CustomRNNCell(nn.Module):

    def __init__(self, input_size, hidden_size):
        super(CustomRNNCell, self).__init__()
        # Maps current input → hidden space  (includes bias b)
        self.W_ih = nn.Linear(input_size, hidden_size, bias=True)
        # Maps previous hidden state → hidden space  (no extra bias to avoid redundancy)
        self.W_hh = nn.Linear(hidden_size, hidden_size, bias=False)

    def forward(self, x_t, h_prev):

        return torch.tanh(self.W_ih(x_t) + self.W_hh(h_prev))


class CustomRNN(nn.Module):

    def __init__(self, input_size, hidden_size, prediction_horizon):
        super(CustomRNN, self).__init__()
        self.hidden_size = hidden_size
        self.cell        = CustomRNNCell(input_size, hidden_size)
        self.fc          = nn.Linear(hidden_size, prediction_horizon)

    def forward(self, x):

        batch_size = x.size(0)
        seq_len    = x.size(1)

        # h_0: zero initialization — model starts with blank memory
        h = torch.zeros(batch_size, self.hidden_size, device=x.device)

        # Unroll across time dimension
        for t in range(seq_len):
            x_t = x[:, t, :]       # (batch, input_size) — one step at a time
            h   = self.cell(x_t, h)  # update hidden state (memory)

        # h now summarizes the entire input sequence
        # Project to prediction_horizon outputs
        return self.fc(h)          # (batch, prediction_horizon)


class LSTMModel(nn.Module):

    def __init__(self, input_size, hidden_size, prediction_horizon):
        super(LSTMModel, self).__init__()
        self.lstm = nn.LSTM(input_size, hidden_size,
                            num_layers=1, batch_first=True)
        self.fc   = nn.Linear(hidden_size, prediction_horizon)

    def forward(self, x):
        # out: (batch, seq, hidden) — output at each step
        # h_n: (layers, batch, hidden) — final hidden state
        out, (h_n, c_n) = self.lstm(x)
        return self.fc(out[:, -1, :])   # use last step's output


class PositionalEncoding(nn.Module):

    def __init__(self, d_model, max_len=500):
        super(PositionalEncoding, self).__init__()
        pe       = torch.zeros(max_len, d_model)
        position = torch.arange(0, max_len).unsqueeze(1).float()
        div_term = torch.exp(
            torch.arange(0, d_model, 2).float() * (-math.log(10000.0) / d_model)
        )
        pe[:, 0::2] = torch.sin(position * div_term)
        # Handle odd d_model safely
        pe[:, 1::2] = torch.cos(position * div_term[:pe[:, 1::2].shape[1]])
        self.register_buffer('pe', pe.unsqueeze(0))  # (1, max_len, d_model)

    def forward(self, x):
        return x + self.pe[:, :x.size(1), :]


class TransformerModel(nn.Module):

    def __init__(self, input_size, hidden_size, prediction_horizon,
                 nhead=2, num_layers=1):
        super(TransformerModel, self).__init__()
        # Project scalar input → hidden_size (required for multi-head attention)
        self.input_proj  = nn.Linear(input_size, hidden_size)
        self.pos_enc     = PositionalEncoding(hidden_size)

        encoder_layer = nn.TransformerEncoderLayer(
            d_model=hidden_size, nhead=nhead,
            dim_feedforward=hidden_size * 4,
            dropout=0.1, batch_first=True
        )
        self.transformer = nn.TransformerEncoder(encoder_layer, num_layers=num_layers)
        self.fc          = nn.Linear(hidden_size, prediction_horizon)

    def forward(self, x):
        x = self.input_proj(x)       # (batch, seq, hidden_size)
        x = self.pos_enc(x)          # add positional signals
        x = self.transformer(x)      # self-attention across all time steps
        return self.fc(x[:, -1, :]) # predict from last position




def train_model(model, X_train, y_train, epochs=150, lr=0.001, batch_size=32):

    optimizer = optim.Adam(model.parameters(), lr=lr)
    criterion = nn.MSELoss()
    dataset   = TensorDataset(X_train, y_train)
    loader    = DataLoader(dataset, batch_size=batch_size, shuffle=True)

    losses = []
    model.train()

    for epoch in range(epochs):
        epoch_loss = 0.0
        for X_batch, y_batch in loader:
            optimizer.zero_grad()
            pred = model(X_batch)
            loss = criterion(pred, y_batch)
            loss.backward()
            # Clip gradients — critical for vanilla RNN stability
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()
            epoch_loss += loss.item()
        avg = epoch_loss / len(loader)
        losses.append(avg)
        if (epoch + 1) % 30 == 0:
            print(f"    Epoch {epoch+1:3d}/{epochs}  loss={avg:.6f}")

    return losses


def evaluate_model(model, X_test, y_test, scaler):

    model.eval()
    with torch.no_grad():
        pred   = model(X_test).numpy()    # (N, horizon)
        actual = y_test.numpy()           # (N, horizon)

    # Flatten horizon dimension for inverse transform
    pred_flat   = pred.reshape(-1, 1)
    actual_flat = actual.reshape(-1, 1)

    pred_orig   = scaler.inverse_transform(pred_flat).flatten()
    actual_orig = scaler.inverse_transform(actual_flat).flatten()

    mse  = mean_squared_error(actual_orig, pred_orig)
    mae  = mean_absolute_error(actual_orig, pred_orig)
    rmse = np.sqrt(mse)

    return pred_orig, actual_orig, {'MSE': mse, 'MAE': mae, 'RMSE': rmse}




def run_full_pipeline(raw_data, dataset_label, window_size,
                      prediction_horizon, hidden_size, epochs=150):
    print(f"\n{'═'*62}")
    print(f"  PIPELINE: {dataset_label}")
    print(f"  window_size={window_size}  |  horizon={prediction_horizon}  |  hidden={hidden_size}")
    print(f"{'═'*62}")

    X_train, X_test, y_train, y_test, scaler = preprocess_data(
        raw_data, window_size, prediction_horizon
    )

    # Instantiate all four models
    model_dict = {
        'MLP'        : MLP(window_size, hidden_size, prediction_horizon),
        'CustomRNN'  : CustomRNN(1, hidden_size, prediction_horizon),
        'LSTM'       : LSTMModel(1, hidden_size, prediction_horizon),
        'Transformer': TransformerModel(1, hidden_size, prediction_horizon),
    }

    all_losses  = {}
    all_results = {}

    for name, model in model_dict.items():
        print(f"\n  ▶  Training {name}...")
        losses = train_model(model, X_train, y_train, epochs=epochs)
        all_losses[name] = losses

        pred, actual, metrics = evaluate_model(model, X_test, y_test, scaler)
        all_results[name] = {'pred': pred, 'actual': actual, 'metrics': metrics}

        print(f"     MSE={metrics['MSE']:.4f}  MAE={metrics['MAE']:.4f}  RMSE={metrics['RMSE']:.4f}")

    # ── Plot 1: Training Loss ─────────────────────────────────
    fig, axes = plt.subplots(2, 2, figsize=(12, 8))
    axes = axes.flatten()
    colors_loss = ['#4C72B0', '#DD8452', '#55A868', '#C44E52']
    for i, (name, losses) in enumerate(all_losses.items()):
        axes[i].plot(losses, color=colors_loss[i], linewidth=1.5)
        axes[i].set_title(f'{name}', fontsize=12, fontweight='bold')
        axes[i].set_xlabel('Epoch')
        axes[i].set_ylabel('MSE Loss')
        axes[i].grid(True, alpha=0.3)
    plt.suptitle(f'Training Loss Curves — {dataset_label}', fontsize=14, fontweight='bold')
    plt.tight_layout()
    save_path = f"plots/{dataset_label.replace(' ', '_')}_loss.png"
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close()
    print(f"\n  ✓ Loss plot saved → {save_path}")

    # ── Plot 2: Predictions vs Actual ─────────────────────────
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    axes = axes.flatten()
    n_show  = min(200, len(all_results['MLP']['actual']))
    actual_ref = all_results['MLP']['actual']  # same for all models

    for i, (name, res) in enumerate(all_results.items()):
        ax = axes[i]
        ax.plot(actual_ref[:n_show], color='black', linewidth=1.5,
                label='Actual', alpha=0.85)
        ax.plot(res['pred'][:n_show], color=colors_loss[i],
                linewidth=1.2, linestyle='--', label=f'{name} Pred')
        m = res['metrics']
        ax.set_title(f"{name}\nRMSE={m['RMSE']:.4f}  MAE={m['MAE']:.4f}",
                     fontsize=10)
        ax.set_xlabel('Test Time Step')
        ax.set_ylabel('Value (original scale)')
        ax.legend(fontsize=8)
        ax.grid(True, alpha=0.3)

    plt.suptitle(f'Prediction vs Actual — {dataset_label}', fontsize=14, fontweight='bold')
    plt.tight_layout()
    save_path = f"plots/{dataset_label.replace(' ', '_')}_predictions.png"
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    plt.close()
    print(f"  ✓ Prediction plot saved → {save_path}")

    return all_results, all_losses




